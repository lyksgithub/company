package com.genepoint.lbsshow.model;

public class MapPath {
	
	private Integer id;
	
	private Integer pathId;
	
	private String building;
	
	private String floor;
	
	private Integer startId;
	
	private Integer endId;
	
	private Double startCorx;
	
	private Double startCory;
	
	private Double endCorx;
	
	private Double endCory;

	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public Integer getPathId() {
		return pathId;
	}

	public void setPathId(Integer pathId) {
		this.pathId = pathId;
	}

	public String getBuilding() {
		return building;
	}

	public void setBuilding(String building) {
		this.building = building;
	}

	public String getFloor() {
		return floor;
	}

	public void setFloor(String floor) {
		this.floor = floor;
	}

	public Integer getStartId() {
		return startId;
	}

	public void setStartId(Integer startId) {
		this.startId = startId;
	}

	public Integer getEndId() {
		return endId;
	}

	public void setEndId(Integer endId) {
		this.endId = endId;
	}

	public Double getStartCorx() {
		return startCorx;
	}

	public void setStartCorx(Double startCorx) {
		this.startCorx = startCorx;
	}

	public Double getStartCory() {
		return startCory;
	}

	public void setStartCory(Double startCory) {
		this.startCory = startCory;
	}

	public Double getEndCorx() {
		return endCorx;
	}

	public void setEndCorx(Double endCorx) {
		this.endCorx = endCorx;
	}

	public Double getEndCory() {
		return endCory;
	}

	public void setEndCory(Double endCory) {
		this.endCory = endCory;
	}

	
	
}
